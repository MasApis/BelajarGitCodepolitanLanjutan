// perkalian
const penjumlahan = (angka1, angka2) => angka1 + angka2;
const pengurangan = (angka1, angka2) => angka1 - angka2;
const perkalian = (angka1, angka2) => angka1 * angka2;
const pembagian = (angka1, angka2) => angka1 / angka2;
console.log("Hasil dari 250 + 200 adalah " + penjumlahan(250, 200));
console.log("Hasil dari 250 - 200 adalah " + pengurangan(250, 200));
console.log("Hasil dari 250 * 200 adalah " + perkalian(250, 200));
console.log("Hasil dari 480 / 20  adalah " + pembagian(480, 20));
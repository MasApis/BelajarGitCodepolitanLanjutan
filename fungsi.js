console.log("Hasil dari 250 - 200 adalah " + pengurangan(250, 200));
console.log("Hasil dari 250 * 200 adalah " + perkalian(250, 200));
console.log("Hasil dari 480 / 20  adalah " + pembagian(480, 20));

